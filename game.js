const TYPE_CHART = {
  'FIRE': { strongAgainst:['PLANT', 'ICE'], weakTo: ['WATER'] },
  'WATER': { strongAgainst: ['FIRE'], weakTo:['PLANT', 'ELECTRIC'] },
  'PLANT': { strongAgainst: ['WATER', 'ELECTRIC'], weakTo: ['FIRE', 'ICE'] },
  'ELECTRIC': { strongAgainst: ['WATER', 'DARK'], weakTo: ['PLANT'] },
  'ICE': { strongAgainst: ['PLANT', 'DARK'], weakTo: ['FIRE'] },
  'DARK': { strongAgainst: ['FIRE', 'ICE'], weakTo:['ELECTRIC'] }
};

function getMultiplier(atkType, defType) {
  if(!TYPE_CHART[atkType]) return 1;
  if(TYPE_CHART[atkType].strongAgainst.includes(defType)) return 1.5;
  if(TYPE_CHART[atkType].weakTo.includes(defType)) return 0.5;
  return 1;
}

